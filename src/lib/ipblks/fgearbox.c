int fgearbox_load_fir(lldev_t dev, unsigned gport, fgearbox_firs_t fir)
{
    const uint8_t* s_st8_dsp;

    switch (fir) {
    case FGBOX_X2:
        s_st8_dsp = s_st8_dsp_fir2x_lines;
        s_st8_dsp = orig;
        break;
    case FGBOX_X128:
        s_st8_dsp = s_st8_dsp_fir128x_lines;
        s_st8_dsp = orig_128;
        break;

    default:
        return -EINVAL;
    }

    return fgearbox_load_ucode(dev, gport, s_st8_dsp);
}







